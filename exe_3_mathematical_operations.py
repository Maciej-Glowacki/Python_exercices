num1 = 10
num2 = 15

sum_value = num1 + num2
quotus = num1 / num2
int_part = num1 % num2

print(f'Sum of {num1} and {num2} = {sum_value} and is:', type(sum_value))
print(f'Division of {num1} and {num2} = {quotus} and is:', type(quotus))
print(f'Modulo of {num1} and {num2} = {int_part} and is:', type(int_part))

